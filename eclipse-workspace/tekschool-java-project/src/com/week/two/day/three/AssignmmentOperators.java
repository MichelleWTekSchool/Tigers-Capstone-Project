package com.week.two.day.three;

public class AssignmmentOperators {
	public static void main(String[] args) {
		
		// += sign *= sign means result X whatever X is is determined by the + + or * sign etc even div
		
		int a = 10;
		int reSult  = 40;
		
		//result = result + a
		reSult += a; //40 + 10 = 50
		
		reSult *= a; // 50 * 10 = 500
		
		System.out.println(reSult);
		
		
	}

}
