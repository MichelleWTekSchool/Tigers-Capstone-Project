package com.week.four.day.two;

public class DeclaringArrayRecap {
	public static void main(String[] args) {
		
		char[][] characters = {
				{'M' , 'N' , 'J' },
				{'U' , 'G' , 'D'},
				{'a' , 'e', 't' },
		};
		
		System.out.println(characters[2][2]);
		//System.out.println(characters[5][5]); prints error to large
		
		System.out.println(characters[0][2]);
				
	}

}
