package com.week.two.day.three;

public class StringEscape {
	public static void main(String[] args) {
		
		//memorize these
		// \n \t \d \" \'
		
		//example \n : will make it go to the next line
		
		System.out.println("Good evening everyone. \n today is wednesday");
		
		//example for \t is one tab
		System.out.println("Good day everyone \t we are gonna have a great day");
		
		//example for \d backspace
		System.out.println("Good Morning\bg Geeks! ");
		
		//example for \" for adding quotation as a special character
		System.out.println("Good evening everyone \"today\" is third day of the week");
				
	}

}
