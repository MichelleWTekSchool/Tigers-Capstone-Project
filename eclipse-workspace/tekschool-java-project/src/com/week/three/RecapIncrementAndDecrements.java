package com.week.three;

public class RecapIncrementAndDecrements {
	public static void main(String[] args) {
		int numbEr = 0;
		
		numbEr ++; // incremented ++; number + number + 1, number += 1 (or whatever number like +=4 etc to go up a #;
		numbEr ++;
		System.out.println(numbEr);
		
		numbEr --; // decremented --
		System.out.println(numbEr);
		
		//prefix and postfix
		System.out.println(--numbEr);//
		
	}

}
