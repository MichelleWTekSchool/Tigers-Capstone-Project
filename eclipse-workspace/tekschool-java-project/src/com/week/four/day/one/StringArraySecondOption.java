package com.week.four.day.one;

public class StringArraySecondOption {
	public static void main(String[] args) {
		
		String[] states = {"Virginia", "California", "Texas", "California", "Washington", "New York"};
		
		//how to find length of an array
		
		int length = states.length;
		
		System.out.println("Length of array " + length);
		
		//print last element in the array
		
		System.out.println(states[length - 1]);
		
		//find specific state
		
		System.out.println(states[4]);
		
		
		
		
		
	}

}
