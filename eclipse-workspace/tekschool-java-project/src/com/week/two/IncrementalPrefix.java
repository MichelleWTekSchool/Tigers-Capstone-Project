package com.week.two;

public class IncrementalPrefix {
	
	public static void main(String[] args) {
		
		int number = 0;
		
		//prefix ++ before the number in this case 10 
		//++ means plus one
		System.out.println( ++ number);
		
		number ++;
		System.out.println(number);
		number ++;
		System.out.println(number);
		number ++;
		number ++;
		System.out.println(number);
	}


}
