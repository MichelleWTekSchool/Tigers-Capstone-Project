package com.week.two.day.two;

public class StringToUpperCase {
	
	public static void main(String[] args) {
		// To upper case will change ALL CHARACTER TO UPPEERCASE
		
		String city = "Austin";
		
		String cityInAllCaps = city.toUpperCase();
		
		System.out.println(cityInAllCaps);
		
		String country = "United States of America";
		
		String countryInAllCaps = country.toUpperCase();
		
		System.out.println(countryInAllCaps);
	}

}
