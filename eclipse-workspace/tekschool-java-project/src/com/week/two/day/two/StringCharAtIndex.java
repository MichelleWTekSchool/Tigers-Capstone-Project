package com.week.two.day.two;

public class StringCharAtIndex {
	
	public static void main(String[] args) {
		//Find Character for given index
		
		String greeting = "Good evening everyone!";
		//Find the character on index 8
		
		char charIndex8 =greeting.charAt(8);
		
		System.out.println(charIndex8);
		
		char charInIndex11 = greeting.charAt(11);
		System.out.println(charInIndex11);
		
	}

}
