package com.week.two;

public class ArithmaticOverview {
	
	public static void main(String[] args) {

	// + used for addition
	
	int numberOne = 400;
	
	int numberTwo = 800;
	
	int result = numberOne + numberTwo;
	
	System.out.println(result);
	
	// - used for subtraction
	
	int numberThree = 1200;
	
	int numberFour = 600;
	
	int resultOfSubtraction = numberThree - numberFour;
	
	//ugly code
	System.out.println(numberThree-numberFour);
	
	//clean
	System.out.println(resultOfSubtraction);
	}
}
