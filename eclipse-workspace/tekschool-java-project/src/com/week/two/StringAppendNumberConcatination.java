package com.week.two;

public class StringAppendNumberConcatination {
	
	public static void main(String[] args) {
		
		int numberA = 10;
		int numberB = 20;
		
		int result = numberA + numberB;
		
		//Not clear what the result is for
		System.out.println(result);
		
		//making results clearer by appending a text to a number
		System.out.println("Result of Adding Two Numbers:" + result);
		
		//even cleaner code
		System.out.println("result of Adding Two Numbers ::: " + result);
		
	}

}
